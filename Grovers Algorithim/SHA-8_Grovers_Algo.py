from qiskit import QuantumCircuit, QuantumRegister, ClassicalRegister, transpile
from qiskit_aer import AerSimulator
import operator

# Qubit Spatial Test
key = QuantumRegister(8, 'key')
tgt = QuantumRegister(1, 'tgt')
out = ClassicalRegister(8, 'meas')
qc = QuantumCircuit(key, tgt, out)

qc.h(key)
qc.x(tgt)
qc.h(tgt)

targethash = '01001011'

# Oracle Function
def apply_oracle(circuit):
    # Forward non-linear hash
    # Bit 0 is the anchor. Bit 1 is a linear XOR.
    circuit.cx(key[0], key[1])
    
    # Bits 2-7 use CCX (Toffoli) for non-linearity
    # Each bit is XORed with the 'AND' of the two previous hash bits
    for i in range(2, 8):
        circuit.ccx(key[i-2], key[i-1], key[i])

    # Zeros in '01001011' are at bits 0, 2, 3, and 5
    hash_zeros = [0, 2, 3, 5]
    
    for bit in hash_zeros:
        circuit.x(key[bit])

    # Phase Kickback
    circuit.mcx(key[0:8], tgt[0])
    
    for bit in hash_zeros:
        circuit.x(key[bit])

    # Reverse the hash function logic
    for i in range(7, 1, -1):
        circuit.ccx(key[i-2], key[i-1], key[i])
    circuit.cx(key[0], key[1])

# Diffuser Function
def apply_diffuser(circuit):
    circuit.h(key)
    circuit.x(key)
    circuit.h(key[7])
    circuit.mcx(key[0:7], key[7])
    circuit.h(key[7])
    circuit.x(key)
    circuit.h(key)

# For 8 bits, 12 iterations is the mathematical peak
iterations = 11
for _ in range(iterations):
    apply_oracle(qc)
    apply_diffuser(qc)

qc.measure(key, out)

# Resuts
simulator = AerSimulator()
compiled_qc = transpile(qc, simulator)
result = simulator.run(compiled_qc, shots=2048).result()
counts = result.get_counts()

fixed_counts = {k[::-1]: v for k, v in counts.items()}
sorted_counts = sorted(fixed_counts.items(), key=operator.itemgetter(1), reverse=True)

print(f"\n8-Bit Non-Linear Key Crack")
print(f"Targeted Hash: {targethash}")
for i in range(0,40): 
    print("-", end="")
print("-")

for i in range(min(3, len(sorted_counts))):
    state, count = sorted_counts[i]
    print(f"Rank {i+1}: Key {state} | Probability: {(count/2048)*100:.1f}%")

